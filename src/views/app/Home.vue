<template>
    <div class="dashboard-container" v-if="meta.data_loaded">
        <!-- github角标 -->
        <!-- <github-corner class="github-corner" /> -->
        <!-- 用户信息 -->
        <el-row v-if="settings.data[SetID.USER_INFO].value" class="mb-4">
            <el-card class="w-full mb-2">
                <div class="flex justify-between flex-wrap">
                    <div class="flex items-center">
                        <el-avatar :size="60" :src="meta.user.avatar" />
                        <router-link style="color:dodgerblue;" to="/userInfo">{{ meta.user.last_name }}{{
                            meta.user.first_name }}</router-link>
                    </div>
                    <div class="flex items-center">
                        <el-link type="danger" size="large" @click="onLinkClicked(0)">{{ meta.title.quote }}</el-link>
                    </div>
                    <div class="flex items-center">
                        <el-link type="success" size="large" @click="onLinkClicked(1)">{{ meta.title.greeting }}</el-link>
                    </div>
                </div>
            </el-card>
        </el-row>

        <!-- big day -->
        <el-scrollbar v-if="settings.data[SetID.BIG_DAY].value">
            <!-- <div style="display: flex; width: 100%;"> -->
                <el-row :gutter="8" class="mb-4" style="display: flex; flex-wrap: nowrap;">
                    <el-col v-for="d in meta.big_days" :xs="8" :sm="6" :md="4" :lg="3" :xl="2" class="mb-2">
                        <BigDay :title='d.name' :date='d.date' :description="d.description"></BigDay>
                    </el-col>
                </el-row>
            <!-- </div> -->
        </el-scrollbar>

        <!-- habbit -->
        <el-row v-if="settings.data[SetID.HABBIT].value" :gutter="8" class="mb-4">
            <el-col :xs="24" :lg="24" class="mb-2">
                <HabbitVue />
            </el-col>
        </el-row>

        <!-- 数据卡片 -->
        <el-row v-if="settings.data[SetID.DATA].value" :gutter="8" class="mb-4">
            <el-col v-for="d in meta.statics" :xs="12" :sm="6" :lg="6" class="mb-2">
                <DataCard :title="d.title" :data="d.data" :icon="d.icon" @click="d.click"></DataCard>
            </el-col>
        </el-row>

        <el-row v-if="settings.data[SetID.RECODRD].value" :gutter="8">
            <!-- Echarts 图表 -->
            <el-col :sm="12" class="mb-4">
                <BarChart id="barChart" :title="meta.chart_title" height="400px" width="100%" :options="meta.exam_his_chart"
                    class="bg-[var(--el-bg-color-overlay)]" />
            </el-col>

            <!-- 表格记录 -->
            <el-col :sm="12" class="mb-4">
                <el-card>
                    <div class="subject">
                        科目: 
                        <el-radio-group v-model="meta.current_sub" size="small">
                            <el-radio-button v-for="(val, key) in meta.exam_his" :label="key"> {{ key }}({{ val.length
                            }})</el-radio-button>
                        </el-radio-group>
                    </div>
                    <el-table :data="meta.exam_his[meta.current_sub]" table-layout="auto" height="404"
                        style="width: 100%; color:darkslategray; font-size: 12px;" stripe border>
                        <!-- <el-table-column fixed type='index' width="30" /> -->
                        <el-table-column prop="quiz_name" sortable label="试卷名" />
                        <el-table-column prop="abs_score" sortable label="得分" />
                        <el-table-column prop="use_minutes" sortable label="分钟" />
                        <el-table-column prop="date_time" sortable label="考试时间" />
                        <el-table-column prop="note" label="备注" />
                    </el-table>
                </el-card>
            </el-col>
        </el-row>
    </div>
    <el-dialog v-model="showSettings" title="配置" width="80%" center>
        <el-form label-position="right" :label-width="200">
            <el-form-item v-for="s in settings.data" :label="s.name">
                <el-switch v-model="s.value" />
            </el-form-item>
            <el-form-item label="操作">
                <el-button type="primary" @click="saveSettings">保存</el-button>
            </el-form-item>
        </el-form>
    </el-dialog>
    <el-affix position="bottom" :offset="20">
        <el-tooltip placement="right" content="配置页面">
            <el-button type="primary" :icon="Setting" circle @click="showSettings = true" />
        </el-tooltip>
    </el-affix>
</template>

<script lang="ts" setup>
import { onMounted, reactive, ref } from 'vue';
import DataCard from '@/components/DataCard.vue';
import BarChart from '@/components/BarChart.vue';
import BigDay from '@/components/BigDay.vue';
import { Api } from '@/request';
import { IOverviewInfo } from '@/types/http';
import { ElMessage } from 'element-plus';
import { ADate } from '@/utils/date';
import HabbitVue from './Habbit.vue';
import { Setting } from '@element-plus/icons-vue'
import { Settings, SetID } from '@/types/settings';
import { useRouter } from 'vue-router';

const router = useRouter()

let settings = reactive(new Settings())
const showSettings = ref(false)

const meta: any = reactive({
    version: '1.7.20230609',
    title: { greeting: '', quote: '' },
    user: { id: 0, avatar: '@/assets/vue.svg', first_name: '', 'last_name': '' },
    big_days: [],
    statics: [
        { title: '科目数', icon: 'Notebook', data: 0 },
        { title: '试卷数', icon: 'Document', data: 0, click: () => { router.push('/examList') } },
        { title: '题库数', icon: 'Collection', data: 0, click: () => { router.push('/questionBank') } },
        { title: '错题数', icon: 'FolderDelete', data: 0, click: () => { router.push('/wrongSet') } },
    ],
    quotes: [
        "不积跬步无以至千里，不积小流无以成江海。——荀子",
        "一寸光阴一寸金，寸金难买寸光阴。——增广贤文",
        "欲穷千里目，更上一层楼。——王之涣",
        "丈夫志四海，万里犹比邻。——曹植",
        "千里之行，始于足下。——老子",
        "吾生也有涯，而知也无涯。——庄子",
        "博观约取，厚积薄发。——苏轼"
    ],
    current_sub: '',
    exam_his: {
    },
    data_loaded: false,
    chart_display_num: 20,
    chart_title: '',
    exam_his_chart: {
        grid: { left: '1%', right: '1%', bottom: '8%', containLabel: true },
        tooltip: {
            trigger: 'axis',
            axisPointer: { type: 'cross', crossStyle: { color: '#999' } },
            formatter: (params: any) => {
                var str: string = '<span style="font-size: 12px;">'
                params.forEach((p: {
                    marker: any; seriesName: any; data: {
                        quiz: string; date_time: string; use_mins: number; value: any; d: any;
                    };
                }) => {
                    if (p.data.use_mins > 0) {
                        str += p.marker + p.seriesName + `【${p.data.quiz}】<br/>`
                        str += '考试时间: ' + p.data.date_time + '<br/>'
                        str += '用时: ' + p.data.use_mins + '分钟<br/>'
                    } else {
                        str += p.marker + p.seriesName + '【暂无数据】<br/>'
                    }
                })
                str += '</span>'
                return str
            }
        },
        legend: {
            x: 'center',
            y: 'bottom',
            data: [],
            textStyle: {
                color: '#999'
            }
        },
        xAxis: [
            {
                type: 'category',
                data: [],
                axisPointer: {
                    type: 'shadow'
                }
            }
        ],
        yAxis: [
            {
                type: 'value',
                name: '分数',
                min: 0,
                max: 100,
                interval: 10,
                axisLabel: {
                    formatter: '{value}'
                }
            }
        ],
        series: []
    }
})

const onLinkClicked = (index: number) => {
    switch (index) {
        case 0:
            meta.title.quote = meta.quotes[Math.floor(Math.random() * meta.quotes.length)]
            break
        case 1:
            var date = new Date()
            let h = date.getHours()
            if (h >= 6 && h < 8) {
                meta.title.greeting = "晨起披衣出草堂，轩窗已自喜微凉🌅！";
            } else if (h >= 8 && h < 12) {
                meta.title.greeting = "上午好🌞！";
            } else if (h >= 12 && h < 18) {
                meta.title.greeting = "下午好☕！";
            } else if (h >= 18 && h < 24) {
                meta.title.greeting = "晚上好🌃！";
            } else if (h >= 0 && h < 6) {
                meta.title.greeting = "偷偷向银河要了一把碎星，只等你闭上眼睛撒入你的梦中，晚安🌛！";
            }
            ElMessage.success({
                message: `${date.toLocaleString('zh-CN')} ${meta.title.greeting}[${meta.version}]`,
                duration: 1000
            })
            break
    }
}

onMounted(async () => {
    onLinkClicked(0)
    onLinkClicked(1)
    var ui = Api.loadUserInfoFromStorage()
    if (ui == null) {
        return;
    }
    meta.user.id = ui.id
    meta.user.first_name = ui.first_name
    meta.user.last_name = ui.last_name
    meta.user.avatar = ui.avatar
    meta.data_loaded = false
    var s = Api.loadSettings()
    s && settings.copyFrom(s)
    await Api.getOverviewInfo().then(res => {
        let info: IOverviewInfo = res.data
        meta.statics[0].data = info.subject_num
        meta.statics[1].data = info.quiz_num
        meta.statics[2].data = info.question_num
        meta.statics[3].data = info.wrongset_num
        meta.exam_his = {}
        var keys = Object.keys(info.exam_record)
        keys.forEach(k => {
            if (info.exam_record[k].length > 0) {
                let scores = []
                meta.exam_his[k] = info.exam_record[k]
                for (var i = 0; i < meta.chart_display_num; i++) {
                    scores.push(i < info.exam_record[k].length ? {
                        'value': info.exam_record[k][i].abs_score, 'date_time': info.exam_record[k][i].date_time,
                        'quiz': info.exam_record[k][i].quiz_name, 'use_mins': info.exam_record[k][i].use_minutes
                    } : { 'value': 0, 'date_time': 'NA', 'quiz': 'NA', 'use_mins': 0 })
                }
                meta.exam_his_chart.series.push({
                    name: k,
                    type: 'line',
                    data: scores.reverse(),
                    label: { show: true },
                    smooth: true
                })
            }
        })
        keys = Object.keys(meta.exam_his)
        if (keys.length > 0) {
            meta.current_sub = keys[0]
        }
        meta.exam_his_chart.legend.data = keys
        for (var i = 0; i < meta.chart_display_num; i++) {
            meta.exam_his_chart.xAxis[0].data.push(`${i + 1}`)
        }
        meta.chart_title = `考试记录(近${meta.chart_display_num}次)`
    }
    ).catch(err => {
        ElMessage.error('统计数据加载失败', err.status)
        return
    })

    await Api.getBigDays().then(res => {
        var now_str = (new ADate()).toString()
        var afterdays: any[] = []
        var beforedays: any[] = []
        res.data.results.forEach((d: { date: string; }) => {
            if (d.date >= now_str) {
                afterdays.push(d)
            } else if (settings.data[SetID.SHOW_FINISHED_DAY].value) {
                beforedays.push(d)
            }
        })
        meta.big_days = afterdays.concat(beforedays.reverse())
        // var lack_num = meta.big_days.length % 8
        // if (lack_num > 0) {
        //     for (var i = 0; i < 8 - lack_num; i++) {
        //         meta.big_days.push({ name: 'TO BE ADDED', description: '待添加', date: '--' })
        //     }
        // }
    }
    ).catch(err => {
        ElMessage.error('纪念日数据加载失败', err.status)
        return
    })
    meta.data_loaded = true
})

const saveSettings = () => {
    Api.storeSettings(settings)
    ElMessage.success('配置保存成功')
    showSettings.value = false
}

</script>

